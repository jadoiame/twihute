<h2>GEOGRAPHY OF RWANDA</h2>
<p>Rwanda is a landlocked country situated in central Africa. Also known as ’the land of a thousand hills’, Rwanda has five volcanoes, twenty-three lakes and numerous rivers, some forming the source of the River Nile. The country lies 75 miles south of the equator in the Tropic of Capricorn, 880 miles ’as the crow flies’ west of the Indian Ocean and 1,250 miles east of the Atlantic Ocean - literally in the heart of Africa. Rwanda is bordered by Uganda to the north, Tanzania to the east, Burundi to the south and the democratic republic of Congo to the west.</p>

<p>Anyone visiting ’the land of a thousand hills’ is in for a multitude of surprises. The loveliness and variety of the landscapes in this ’green country’ is dominated to the north by volcanoes and bordered by Lake Kivu to the west. In Rwanda the great animals of the wild are protected from poachers and roam free in the vast national parks. The Volcanoes National park in the Virunga volcanic mountains with its high altitude forests, are world famous for mountain gorillas - timid and passive family oriented giants - which is teeming with wildlife both large and small, while Lake Kivu to the west offers beautiful beaches, jutting pennisulas and an archipelago of islands.</p>


<h2>BRIEF HISTORY OF RWANDA</h2>

<p>For centuries, Rwanda existed as a centralized monarchy under a succession of Tutsi kings from one clan, who ruled through cattle chiefs, land chiefs and military chiefs. The king was supreme but the rest of the population, Bahutu, Batutsi and Batwa, lived in symbiotic harmony. In 1899, Rwanda became a German colony and, in 1919, the system of indirect rule continued with Rwanda as a mandate territory of the League of Nations, under Belgium. From 1959, Batutsi were targeted, causing hundreds of thousands of deaths and sending almost two million of them into exile. The First Republic, under President Gregoire Kayibanda, and the second, under President Juvenal Habyarimana, institutionalized discrimination against Batutsi and subjected them to periodmassacres.</p>

<p>The Rwandese Alliance for National Unity (RANU) was formed in 1979 by Rwandan refugees in exile, to mobilize against divisive politics and genocide ideology, repeated massacres, statelessness and the lack of peaceful political exchange.. In 1987, RANU became the Rwandese Patriotic Front (RPF). On 1 October 1990, the RPF launched an armed liberation struggle that ultimately ousted the dictatorship in 1994 and ended the genocide of more than one million Batutsi and massacres of moderate Bahutu who opposed the genocide.</p>

<p>After Kigali fell to RPA (RPF’s armed wing) on 4 July 1994, RPF formed a Government of National Unity headed by President Pasteur Bizimungu, bringing parties that did not participate in the genocide together. In 2000, Parliament voted out President Pasteur Bizimungu and RPF appointed then Vice-President and Minister of Defense, Major General Paul Kagame as the President of the Republic to lead the coalition government. In 2003 President Paul Kagame was elected with landslide majority to serve a term of seven years. During those seven years, the country made unprecedented socio-economic and political progress and consolidated peace, stability as well as social cohesion among Rwandans.. In 2010, President Paul Kagame was re-elected to serve a second term and on a platform of rapid development for the transformation of the lives of all Rwandans.</p>

